/**
 *      Default constructor, allocates storage for a mutex.
 */
CScopeMutex::CScopeMutex( CMutex& mutex )
        : m_mutex( &mutex )
{
        assert( m_mutex );
        
        m_mutex->Lock();
}

/*--------------------------------------------------------------------------*/

/**
 *      Destructor, de-allocates storage for a mutex.
 */
CScopeMutex::~CScopeMutex()
{
        m_mutex->Unlock();
}

