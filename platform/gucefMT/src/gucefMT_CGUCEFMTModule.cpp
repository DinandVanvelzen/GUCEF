bool
CGUCEFMTModule::Load( void )
{
    // make sure the precision time is initialized
    PrecisionTimerInit();

    return true;
}

/*-------------------------------------------------------------------------*/

bool
CGUCEFMTModule::Unload( void )
{
    // shutdown the performance timer last
    PrecisionTimerShutdown();

    return true;
}

